public class Book{
    public String title;
    public String author;
    public String isbn;
    public String  year;
    public String publisher;
    public String pages;
    

    public Book(String title,String author,String isbn,String year,String publisher,String pages)
    {
        this.title = title;
        this.author = author;
        this.isbn = isbn;
        this.year = year;
        this.publisher=publisher;
        this.pages = pages;
    }
}

